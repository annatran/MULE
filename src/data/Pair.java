package data;

/**
 * Represents an x,y coordinate pair
 *
 */
public class Pair {
	/**
	 * x position of object
	 */
	public int x;
	/**
	 * y position of object
	 */
	public int y;
	
	/**
	 * 
	 * @param x coordinate of pair
	 * @param y coordinate of pair
	 */
	public Pair(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
}
